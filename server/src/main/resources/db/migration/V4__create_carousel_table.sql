-- 3. Atualiza os caminhos das imagens existentes de /images/... para /media/{filename}
UPDATE tb_product 
SET image_url = '/media/' || substring(image_url from '[^/]+$')
WHERE image_url IS NOT NULL AND image_url LIKE '%/%';

UPDATE tb_product_images 
SET image_url = '/media/' || substring(image_url from '[^/]+$')
WHERE image_url IS NOT NULL AND image_url LIKE '%/%';
