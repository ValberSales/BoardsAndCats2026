-- 1. Criação da tabela de itens do carousel/banner principal
CREATE TABLE tb_carousel_item (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id bigint,
    image_url varchar(255) NOT NULL,
    alt varchar(255) NOT NULL,
    CONSTRAINT fk_carousel_item_product FOREIGN KEY (product_id) REFERENCES tb_product(id) ON DELETE SET NULL
);

